package com.hxy.isw.entity;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "salesmanchangelog")
public class SalesmanChangeLog {
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	@Column(name = "id")
	private long id;
	
	@Column(name = "salesmanchangeid")
	private long salesmanchangeid;
	
	@Column(name = "fsalesmanid")
	private long fsalesmanid;	
	
	@Column(name = "outfcompanyid")
	private long outfcompanyid;	
	
	@Column(name = "outfaccountid")
	private long outfaccountid;	
	
	@Column(name = "infcompanyid")
	private long infcompanyid;
	
	@Column(name = "infaccountid")
	private long infaccountid;	
	
	@Column(name = "fdoitid")
	private long fdoitid;
	
	@Column(name = "createtime")
	private Date createtime;
	
	@Column(name = "state")
	private int state;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	
	public long getSalesmanchangeid() {
		return salesmanchangeid;
	}

	public void setSalesmanchangeid(long salesmanchangeid) {
		this.salesmanchangeid = salesmanchangeid;
	}

	public long getFsalesmanid() {
		return fsalesmanid;
	}

	public void setFsalesmanid(long fsalesmanid) {
		this.fsalesmanid = fsalesmanid;
	}

	public long getOutfcompanyid() {
		return outfcompanyid;
	}

	public void setOutfcompanyid(long outfcompanyid) {
		this.outfcompanyid = outfcompanyid;
	}

	public long getOutfaccountid() {
		return outfaccountid;
	}

	public void setOutfaccountid(long outfaccountid) {
		this.outfaccountid = outfaccountid;
	}

	public long getInfcompanyid() {
		return infcompanyid;
	}

	public void setInfcompanyid(long infcompanyid) {
		this.infcompanyid = infcompanyid;
	}

	public long getInfaccountid() {
		return infaccountid;
	}

	public void setInfaccountid(long infaccountid) {
		this.infaccountid = infaccountid;
	}

	public long getFdoitid() {
		return fdoitid;
	}

	public void setFdoitid(long fdoitid) {
		this.fdoitid = fdoitid;
	}

	public Date getCreatetime() {
		return createtime;
	}

	public void setCreatetime(Date createtime) {
		this.createtime = createtime;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}	

	
}
